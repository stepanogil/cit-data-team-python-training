a = 2
b = 5.0

# basic operations
result_add = a + b
print(result_add)

result_sub = a - b
print(result_sub)

result_mul = a * b
print(result_mul)

result_div = a / b
print(result_div)

# exponentiation
result_exp = a ** b
print(result_exp)

# comparisons
result_eq = a == b
print(result_eq)

result_neq = a != b
print(result_neq)